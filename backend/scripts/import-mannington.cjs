async function upsertAttr(client, skuId, attrId, value) {
  if (!value) return;
  await client.query(`
    INSERT INTO sku_attributes (sku_id, attribute_id, value)
    VALUES ($1, $2, $3)
    ON CONFLICT (sku_id, attribute_id) DO UPDATE SET value = EXCLUDED.value
  `, [skuId, attrId, value]);
}

async function setAttrBySlug(client, skuId, slug, value) {
  if (!value) return;
  const res = await client.query('SELECT id FROM attributes WHERE slug = $1', [slug]);
  if (res.rows.length === 0) return;
  await upsertAttr(client, skuId, res.rows[0].id, value);
}

function formatSize(width, thickness) {
  if (!width) return thickness;
  const w = String(width);
  if (w.includes('x') || w.includes('X')) {
    const [a, b] = w.split(/[xX]/);
    return `${thickness} x ${a}" x ${b}"`;
  }
  return `${thickness} x ${w}" x RL`;
}

function getRetail(mapKey, cost) {
  if (mapKey && MAP[mapKey]) return MAP[mapKey];
  return cost * 2.0;
}

