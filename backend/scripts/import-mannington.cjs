async function processFlooring(client, vendorId, collections, accTypes, defaultAccPrices, stats) {
  for (const col of collections) {
    const accPrices = col.ap || defaultAccPrices;
    const accSeen = {};
    let colSkus = 0, colAcc = 0;

    for (const row of col.s) {
      const [pattern, width, color, colorCode, cost, sqftCtn, ctnPlt, lbsCtn, ...accCats] = row;
      const freight = pattern.endsWith('*');
      const cleanPattern = freight ? pattern.slice(0, -1) : pattern;

      // Upsert product
      const prodRes = await client.query(`
        INSERT INTO products (id, vendor_id, name, collection, category_id, status)
        VALUES (gen_random_uuid(), $1, $2, $3, $4, 'active')
        ON CONFLICT ON CONSTRAINT products_vendor_collection_name_unique
        DO UPDATE SET category_id = EXCLUDED.category_id, status = 'active'
        RETURNING id
      `, [vendorId, cleanPattern, col.c, col.k]);
      const productId = prodRes.rows[0].id;
      stats.products++;

      // Upsert flooring SKU
      const intSku = 'MANN-' + colorCode;
      const skuRes = await client.query(`
        INSERT INTO skus (id, product_id, vendor_sku, internal_sku, variant_name, sell_by, status)
        VALUES (gen_random_uuid(), $1, $2, $3, $4, 'sqft', 'active')
        ON CONFLICT ON CONSTRAINT skus_internal_sku_key
        DO UPDATE SET variant_name = EXCLUDED.variant_name, sell_by = 'sqft', status = 'active'
        RETURNING id
      `, [productId, colorCode, intSku, color]);
      const skuId = skuRes.rows[0].id;
      stats.skus++;
      colSkus++;

      // Pricing
      const retail = getRetail(col.map, cost);
      const mapPrice = (col.map && MAP[col.map]) ? retail : null;
      await client.query(`
        INSERT INTO pricing (sku_id, cost, retail_price, map_price, price_basis)
        VALUES ($1, $2, $3, $4, 'sqft')
        ON CONFLICT (sku_id) DO UPDATE SET cost = EXCLUDED.cost,
          retail_price = EXCLUDED.retail_price, map_price = EXCLUDED.map_price
      `, [skuId, cost.toFixed(2), retail.toFixed(2), mapPrice ? mapPrice.toFixed(2) : null]);
      stats.pricing++;

      // Packaging
      const sqftPlt = (sqftCtn * ctnPlt).toFixed(2);
      await client.query(`
        INSERT INTO packaging (sku_id, sqft_per_box, boxes_per_pallet, sqft_per_pallet, weight_per_box_lbs)
        VALUES ($1, $2, $3, $4, $5)
        ON CONFLICT (sku_id) DO UPDATE SET sqft_per_box = EXCLUDED.sqft_per_box,
          boxes_per_pallet = EXCLUDED.boxes_per_pallet, sqft_per_pallet = EXCLUDED.sqft_per_pallet,
          weight_per_box_lbs = EXCLUDED.weight_per_box_lbs
      `, [skuId, sqftCtn, ctnPlt, sqftPlt, lbsCtn]);
      stats.pkg++;

      // Attributes
      await upsertAttr(client, skuId, ATTR.color, color);
      await upsertAttr(client, skuId, ATTR.thickness, col.t);
      await upsertAttr(client, skuId, ATTR.size, formatSize(width, col.t));
      if (col.mat) await setAttrBySlug(client, skuId, 'material', col.mat);
      if (freight) await setAttrBySlug(client, skuId, 'freight_upcharge', 'true');

      // Accessories — dedupe per product
      if (!accTypes || !accPrices) continue;
      const pk = productId;
      if (!accSeen[pk]) accSeen[pk] = new Set();
      for (let i = 0; i < accTypes.length; i++) {
        const catNum = accCats[i];
        if (!catNum || accSeen[pk].has(catNum)) continue;
        accSeen[pk].add(catNum);

        const accInternal = 'MANN-' + catNum;
        const accCost = accPrices[i];
        const accRetail = (accCost * 2.0).toFixed(2);

        const accRes = await client.query(`
          INSERT INTO skus (id, product_id, vendor_sku, internal_sku, variant_name, sell_by, variant_type, status)
          VALUES (gen_random_uuid(), $1, $2, $3, $4, 'unit', 'accessory', 'active')
          ON CONFLICT ON CONSTRAINT skus_internal_sku_key
          DO UPDATE SET variant_name = EXCLUDED.variant_name, sell_by = 'unit',
                       variant_type = 'accessory', status = 'active'
          RETURNING id
        `, [productId, catNum, accInternal, accTypes[i]]);
        const accSkuId = accRes.rows[0].id;

        await client.query(`
          INSERT INTO pricing (sku_id, cost, retail_price, price_basis)
          VALUES ($1, $2, $3, 'unit')
          ON CONFLICT (sku_id) DO UPDATE SET cost = EXCLUDED.cost, retail_price = EXCLUDED.retail_price
        `, [accSkuId, accCost.toFixed(2), accRetail]);
        stats.acc++;
        stats.pricing++;
        colAcc++;
      }
    }
    console.log(`  ${col.c}: ${colSkus} SKUs + ${colAcc} accessories`);
  }
}

async function processSundries(client, vendorId, stats) {
  for (const [desc, catalogNum, skuCode, costUnit, unitSize, coverage] of SUNDRIES) {
    // Determine category from description
    let catId = CAT.inst;
    const d = desc.toLowerCase();
    if (d.includes('adhesive') || d.includes('glue') || d.includes('grout') || d.includes('sealer') || d.includes('seam')) catId = CAT.adh;
    if (d.includes('underlayment') || d.includes('barrier') || d.includes('silencer') || d.includes('whisper')) catId = CAT.und;

    // Upsert product (one product per sundry)
    const prodRes = await client.query(`
      INSERT INTO products (id, vendor_id, name, collection, category_id, status)
      VALUES (gen_random_uuid(), $1, $2, 'Sundries', $3, 'active')
      ON CONFLICT ON CONSTRAINT products_vendor_collection_name_unique
      DO UPDATE SET category_id = EXCLUDED.category_id, status = 'active'
      RETURNING id
    `, [vendorId, desc, catId]);
    const productId = prodRes.rows[0].id;
    stats.products++;

    // Upsert SKU
    const intSku = 'MANN-' + skuCode;
    const skuRes = await client.query(`
      INSERT INTO skus (id, product_id, vendor_sku, internal_sku, variant_name, sell_by, status)
      VALUES (gen_random_uuid(), $1, $2, $3, $4, 'unit', 'active')
      ON CONFLICT ON CONSTRAINT skus_internal_sku_key
      DO UPDATE SET variant_name = EXCLUDED.variant_name, sell_by = 'unit', status = 'active'
      RETURNING id
    `, [productId, catalogNum, intSku, `${unitSize || ''}`]);
    const skuId = skuRes.rows[0].id;
    stats.skus++;

    // Pricing
    await client.query(`
      INSERT INTO pricing (sku_id, cost, retail_price, price_basis)
      VALUES ($1, $2, $3, 'unit')
      ON CONFLICT (sku_id) DO UPDATE SET cost = EXCLUDED.cost, retail_price = EXCLUDED.retail_price
    `, [skuId, costUnit.toFixed(2), (costUnit * 2.0).toFixed(2)]);
    stats.pricing++;

    // Coverage attribute
    if (coverage) await setAttrBySlug(client, skuId, 'coverage', coverage);
  }
  console.log(`  Sundries: ${SUNDRIES.length} items`);
}

async function run() {
  const client = await pool.connect();
  try {
    await client.query('BEGIN');

    // Upsert vendor
    const vRes = await client.query(`
      INSERT INTO vendors (id, name, code, website)
      VALUES (gen_random_uuid(), 'Mannington', 'MANNINGTON', 'https://www.mannington.com')
      ON CONFLICT (code) DO UPDATE SET name = EXCLUDED.name, website = EXCLUDED.website
      RETURNING id
    `);
    const vendorId = vRes.rows[0].id;
    console.log(`Vendor: Mannington (${vendorId})\n`);

    const stats = { products: 0, skus: 0, acc: 0, pricing: 0, pkg: 0 };

    // LVT
    console.log('LVT:');
    await processFlooring(client, vendorId, LVT_COLLECTIONS, LVT_ACC_TYPES, LVT_ACC_PRICES, stats);

    // Laminate
    console.log('\nLaminate:');
    await processFlooring(client, vendorId, LAM_COLLECTIONS, LAM_ACC_TYPES, LAM_ACC_PRICES, stats);

    // Wood
    console.log('\nWood:');
    await processFlooring(client, vendorId, WOOD_COLLECTIONS, WOOD_ACC_TYPES, null, stats);

    // Sundries
    console.log('\nSundries:');
    await processSundries(client, vendorId, stats);

    if (DRY) {
      await client.query('ROLLBACK');
      console.log('\n*** DRY RUN — all changes rolled back ***');
    } else {
      await client.query('COMMIT');
    }

    console.log(`\n=== Import Complete ===`);
    console.log(`Products:       ${stats.products}`);
    console.log(`Flooring SKUs:  ${stats.skus}`);
    console.log(`Accessory SKUs: ${stats.acc}`);
    console.log(`Total SKUs:     ${stats.skus + stats.acc}`);
    console.log(`Pricing:        ${stats.pricing}`);
    console.log(`Packaging:      ${stats.pkg}`);
  } catch (err) {
    await client.query('ROLLBACK');
    throw err;
  } finally {
    client.release();
    await pool.end();
  }
}

run().catch(err => { console.error(err); process.exit(1); });
